export function getMainAirport(countryCode: string): AirportInfo | undefined {
  const normalized = countryCode.toLowerCase();
  const airports = MAIN_AIRPORTS[normalized];
  if (!airports || airports.length === 0) return undefined;
  return airports[0];
}

export function getAirportCoordinates(countryCode: string): [number, number] | undefined {
  const airport = getMainAirport(countryCode);
  if (!airport) return undefined;
  return [airport.lat, airport.lon];
}
